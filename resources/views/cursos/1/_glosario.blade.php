       <h2 class="ModalProgreso-Title">GLOSARIO</h2>

       <div class="ModalGlosario-ContentList u-inline-block u-textJustify">

           <div class="ModalGlosario-ColContentList u-inline-block u-textJustify">
               <p><strong>ANÁLISIS DE RIESGO</strong>
                   <span>Es la aplicación de un examen crítico, sistemático y regular al proceso y a las intenciones técnicas de las instalaciones para evaluar el potencial de riesgo de accidente por un mal funcionamiento de un elemento individual del equipo y sus consecuencias sobre la instalación en conjunto</span>
               </p>
               <p><strong>AMBIENTE</strong>
                   <span>El conjunto de los elementos naturales y artificiales o inducidos por el hombre que hacen posible la existencia y desarrollo de los seres humanos y demás organismos vivos que interactúan en un espacio y tiempo determinados</span>
               </p>
               <p><strong>CÓDIGO</strong>
                   <span> Documento que contiene el conjunto de reglas y procedimientos de carácter legal, las cuales forman un sistema completo aprobado sobre alguna especialidad</span>
               </p>
               <p><strong>NORMA</strong>
                   <span>Documento que contiene un conjunto de reglas que determinan completamente las características de un proceso, artículo, actividad o un producto industrial. (Como se debe hacer o está establecido que se haga una cosa).</span>
               </p>
               <p><strong>NOMEX</strong>
                   <span>Es una marca registrada de un material de aramida resistente a las llamas desarrollado a principio de los años 60 por DuPont.</span>
               </p>
               <p><strong>PEL</strong>
                   <span>(Permissible Exposure Limit) Límite de exposición permitido por la OSHA. Se indica cuento este es más estricto que el TVL.</span>
               </p>
               <p><strong>PRÁCTICAS DE INGENIERÍA</strong>
                   <span>Son desarrolladas por medio de técnicas analíticas fundamentadas basándose en la aplicación normativa, identificando factores de riesgos y sus controles, la producción, calidad y seguridad dentro de los procesos y sus productos.</span>
               </p>
           </div>

           <div class="ModalGlosario-ColContentList u-inline-block u-textJustify u-sinMargen">

               <p><strong>SEGURIDAD INDUSTRIAL </strong>
                   <span>Es el conjunto de conocimientos técnicos que posee un individuo y son aplicados para controlar, reducir y eliminar accidentes en el trabajo. Es la buena administración de normas, códigos, prácticas seguras de trabajo y de ingeniería para prevenir los riesgos inherentes al proceso, preservando la salud de su personal, instalación y medio ambiente.</span>
               </p>
               <p><strong>TVL</strong>
                   <span>(Threshold Limit Values) Valor Umbral Límite. Los límites máximos de exposición en áreas con alguna sustancia contaminante y normalmente se expresan en partes por millón (partes de sustancia contaminante por millón de partes de aire) a menos que se indique específicamente como mg/m3 (miligramos de sustancia contaminante por metro cúbico de aire).</span>
               </p>
               <p><strong>TWA</strong>
                   <span>(Time Weighted Average) Concentraciones Promedio Ponderado en el Tiempo, para una jornada de trabajo normal de 8 horas y una semana 40 horas.</span>
               </p>
               <p><strong>WEEL</strong>
                   <span>(Workplace Environmental Exposure Level) Nivel de Exposición Ambiental en el Ambiente de Trabajo. Se indica para las sustancias que poseen TLV o PEL.</span>
               </p>
           </div>

