      <p class="texto_ayuda">
<strong>¿Puedo avanzar más rápido si tengo experiencia previa sobre los temas de la capacitación?</strong><br/><br/>
Existen dos tipos de experiencia sobre estos temas básicamente. La primera tiene que ver con capacitaciones previas, formales y académicas; la otra se refiere a la formación empírica, en ambos casos recomendamos realizar los ejercicios y las evaluaciones inicialmente para determinar el nivel de conocimiento e identificar las áreas de oportunidad en dónde se deberá hacer un repaso.<br/>
<br/>Idealmente, ya sea que se cuente con poca o mucha experiencia previa, lo recomendable es iniciar la capacitación desde el primer tema.<br/><br/>

<strong>¿Cómo puedo inscribirme a nuevos cursos de capacitación?</strong><br/><br/>
Los cursos de capacitación se encuentran ligados, comparten bases de conocimiento y elementos que nos permiten entender mejor la información dispuesta en cada uno de ellos, para que un nuevo curso sea liberado es necesario haber concluido el anterior con un porcentaje de aprovechamiento igual o superior al promedio que el capacitador establezca.<br/>
<br/>
<strong>¿Cómo funcionan las lecciones y las unidades?</strong><br/><br/>

La capacitación se encuentra dividida en lecciones, las cuales son la unidad mínima de capacitación, un conjunto de lecciones forman una unidad; dependiendo de los temas las unidades tendrán mayor o menor número de lecciones. Al terminar una lección obtienes un porcentaje de avance, algunas lecciones además poseen ejercicios y autoevaluaciones, de tal manera que puedas medir adecuadamente tus conocimientos y avances.<br/><br/>
Al terminar un grupo de lecciones que forman la unidad, se presenta un examen general de la unidad, el cual identifica los conceptos básicos de cada lección y los evalúa, accedes a la siguiente unidad cuando acumulas los puntos necesarios para probar la unidad anterior. No sólo obtienes puntos al realizar las evaluaciones, los puntos se van sumando de acuerdo a las lecciones que has completado y los ejercicios realizados.<br/><br/>

